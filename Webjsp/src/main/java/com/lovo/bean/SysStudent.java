package com.lovo.bean;


public class SysStudent {

  private long sysStuId;
  private String sysStuName;
  private String sysStuUserName;
  private String sysStuPwd;


  public long getSysStuId() {
    return sysStuId;
  }

  public void setSysStuId(long sysStuId) {
    this.sysStuId = sysStuId;
  }


  public String getSysStuName() {
    return sysStuName;
  }

  public void setSysStuName(String sysStuName) {
    this.sysStuName = sysStuName;
  }


  public String getSysStuUserName() {
    return sysStuUserName;
  }

  public void setSysStuUserName(String sysStuUserName) {
    this.sysStuUserName = sysStuUserName;
  }


  public String getSysStuPwd() {
    return sysStuPwd;
  }

  public void setSysStuPwd(String sysStuPwd) {
    this.sysStuPwd = sysStuPwd;
  }

}
